#include <stdio.h>
#include <locale.h>

/* Exercício 02: Escreva um programa que solicite ao usuário um número inteiro e ao final apresente na tela
o número informado pelo usuário do programa */

int main() {
	setlocale(LC_ALL, "Portuguese");
	
	int numero;
	
	printf("Informe um número: ");
	scanf("%i", &numero);
	
	printf("O número informado foi: %i.", numero);
}
